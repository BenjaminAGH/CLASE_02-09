
function uppercase(str) {
    return str.toUpperCase();
}

function lowercase(str) {
    return str.toLowerCase();
}

const palabra = "Hola Mundo";
console.log(uppercase(palabra));
console.log(lowercase(palabra));

function resta(a, b) {
    return a - b;
}

function division(a, b) {
    return a / b;
}

function multiplicacion(a, b) {
    return a * b;
}

console.log(resta(20, 5));
console.log(division(20, 5));
console.log(multiplicacion(20, 5));

function longitud(str) {
    return str.length;
}

console.log(longitud("Hola Mundo"));

